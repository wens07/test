package util_lib

// reverse the string
func StringReverse(str string) string {

	r := []rune(str)

	for i, j := 0, len(r)-1; i < len(r)/2; i, j = i+1, j-1 {
		r[i], r[j] = r[j], r[i]
	}

	return string(r)
}
